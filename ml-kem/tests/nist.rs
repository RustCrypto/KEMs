mod vectors;

use hex_literal::hex;
use ml_kem::*;
use vectors::*;

pub struct Vector {
    pub generate: &'static GenerateVector,
    pub encapsulate: &'static EncapsulateVector,
    pub decapsulate: &'static DecapsulateVector,
}

impl Vector {
    fn verify<K: KemCore>(&self) {
        self.generate.verify::<K>();
        self.encapsulate.verify::<K>();
        self.decapsulate.verify::<K>();
    }
}

pub struct Vectors {
    pub mlkem512: Vector,
    pub mlkem768: Vector,
    pub mlkem1024: Vector,
}

impl Vectors {
    pub fn verify(&self) {
        self.mlkem512.verify::<MlKem512>();
        self.mlkem768.verify::<MlKem768>();
        self.mlkem1024.verify::<MlKem1024>();
    }
}

